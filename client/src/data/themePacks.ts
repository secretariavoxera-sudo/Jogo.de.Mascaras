export const FREE_TOPICS_COUNT = 10;

export const getPackTopics = (packId: string, isPremium: boolean): { topic: string; locked: boolean }[] => {
  const pack = THEME_PACKS.find(p => p.id === packId);
  if (!pack) return [];
  return pack.topics.map((topic, index) => ({
    topic,
    locked: !isPremium && index >= FREE_TOPICS_COUNT,
  }));
};
